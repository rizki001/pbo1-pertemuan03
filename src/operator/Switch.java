package operator;

public class Switch {
    public static void main(String[] args) {
        int jalanJalan=3;
        switch(jalanJalan){
            case 1:
                System.out.println("Orang Free");
                break;
            case 2:
                System.out.println("Orang Pacaran");
                break;
            case 3:
                System.out.println("Orang ketiga");
                break;
            case 4:
                System.out.println("Double Date");
                break;
            default:
                System.out.println("Setan anjay");
                break;

        }
    }
}
